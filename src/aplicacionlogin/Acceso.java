    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        //Si el campo del Usuario esta vacio, desplega mensaje
        if (txtUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un Usuario", "CAMPO OBLIGATORIO*", JOptionPane.ERROR_MESSAGE);
        } else if (txtPassword.getText().isEmpty()) {//Si el campo del password esta vacio, desplega mensaje
            JOptionPane.showMessageDialog(null, "Ingrese un Password", "CAMPO OBLIGATORIO*", JOptionPane.ERROR_MESSAGE);
        } else if (AccesoUsuario.validarPersona(txtUsuario.getText(), txtPassword.getText())) {//Si devuelve True al validar los campos ingresados, executara las siguientes acciones de mas abajo
            org.tempuri.BackendSVC service = new org.tempuri.BackendSVC();
            org.tempuri.IbackendSVC port = service.getBasicHttpBindingIbackendSVC();
            Persona per = port.recuperaDatos(txtUsuario.getText());
            int perfil = per.getIdPerfil();//Usando el WebService, retornara el tipo de fergil del usuario.
            switch (perfil) {//Dependiendo del tipo de perfil, desplegara una accion distinta
                //Caso 1: Perfil Secretario/a
                case 1:
                    clases.FuncionesGenerales.setId(perfil);//Se guardara el perfil en el objeto "Id" de la clase "FuncionesGenerales"
                    clases.FuncionesGenerales.setRut(txtUsuario.getText());//Se guardara el rut en el Objeto "rut"
                    FechaCalendarioBase fcb = new FechaCalendarioBase();
                    fcb.setVisible(true);//Se abre la ventana FechaCalendarioBase
                    this.setVisible(false);//La ventana actual se cierra
                    break;
                //Caso 2: Perfil Medico
                case 2:
                    clases.FuncionesGenerales.setId(perfil);//Se guardara el perfil en el objeto "Id" de la clase "FuncionesGenerales"
                    clases.FuncionesGenerales.setRut(txtUsuario.getText());//Se guardara el rut en el Objeto "rut"
                    VentanaListaDePacientes vpe = new VentanaListaDePacientes();
                    vpe.setVisible(true);//Se habre ventanaMenuMedico
                    this.setVisible(false);//La ventana actual se cierra
                    break;
                //Caso 3: Perfil Cajero
                case 3:
                    clases.FuncionesGenerales.setId(perfil);//Se guardara el perfil en el objeto "Id" de la clase "FuncionesGenerales"
                    clases.FuncionesGenerales.setRut(txtUsuario.getText());//Se guardara el rut en el Objeto "rut"
                    VentanaTablaReservaPorCliente vp = new VentanaTablaReservaPorCliente();
                    vp.setVisible(true);//Se habre la ventana BuscarCLientePago
                    this.setVisible(false);//La ventana actual se cierra
                    break;

                default:
                    break;

            }
        } else {//Se muestra el mensaje si tanto el password como la contrase√±a son invalidos
            JOptionPane.showMessageDialog(this, "Usuario o Password Incorrecto");
            txtUsuario.setText("");
            txtPassword.setText("");
            txtUsuario.requestFocusInWindow();
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

