    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        int opcion = JOptionPane.showConfirmDialog(this, "Esta seguro", "Â¿Seguro?", JOptionPane.YES_NO_OPTION); //Se obtiene una opcion..

        if (opcion == JOptionPane.YES_OPTION) {//Si la opcion es Si, se obtendran los datos de la ventana, y se procedera a crear el pago
            int totalAPagar = Integer.parseInt(txtTotalPagar.getText());
            String formaPago = cmbFormaPago.getSelectedItem().toString();
            String rut = txtRut.getText();
            int idHoraAtencion = Integer.parseInt(id_ag);
            String prevision = cmbPrevision.getSelectedItem().toString();
            int comision = Integer.parseInt(cmbComision.getSelectedItem().toString());
            int idMedico = Integer.parseInt(id_med);
            //Si al crear el pago, se devuelve true, se desplega el mensaje y se devuelve a la ventana BuscarClientePago
            if (clases.FuncionesCajero.crearPago(totalAPagar, formaPago,
                    rut, idHoraAtencion,
                    prevision, comision,
                    idMedico)) {
                JOptionPane.showMessageDialog(this, "Pago creado correctamente");
                this.setVisible(false);
                new VentanaTablaReservaPorCliente().setVisible(true);
            } else {//Sino, se desplega el siguiente mensaje
                JOptionPane.showMessageDialog(this, "Hubo un error");
            }
        }
    }//GEN-LAST:event_btnPagarActionPerformed
    
    
