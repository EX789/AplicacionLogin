    private void tbPacienteAtendidoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbPacienteAtendidoMouseClicked
        if (evt.getClickCount() == 2) {//Si se dan dos clicks a un elemento seleccionado, se executa la siguiente accion
            int row = tbPacienteAtendido.rowAtPoint(evt.getPoint());//Se obtiene la fila
            try {
                if (tbPacienteAtendido.getValueAt(row, 3).toString().equals("Espera")) {
                    int opcion = JOptionPane.showConfirmDialog(this, "Seguro desea modificar ha este paciente", "Â¿Modificar?", JOptionPane.YES_NO_OPTION);//Se pregunta si se quieren modificar los datos
                    if (opcion == JOptionPane.YES_OPTION) {//Si se clickea "Si", se executara la siguiente accion
                        if ((clases.FuncionesMedico.pacienteAtendido(Integer.parseInt(tbPacienteAtendido.getValueAt(row, 5).toString())))) {//Si se satisface la funcon, se obtendra un mensaje y se cargara nuevamente la tabla
                            JOptionPane.showMessageDialog(this, "modificado correctamente");
                            clases.FuncionesPaciente.cargarTabla(clases.FuncionesGenerales.getRut(), clases.FuncionesGenerales.getId(), modelo);
                        } else {
                            JOptionPane.showMessageDialog(this, "No se pudo guardar");//Sino, se obtendra el siguiente mensaje
                        }
                    }
                } else{
                    JOptionPane.showMessageDialog(this, "No se puede actualizar un paciente con el estado '"+tbPacienteAtendido.getValueAt(row, 3).toString()+"'");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error." + e.getMessage());
            }
        }
    }//GEN-LAST:event_tbPacienteAtendidoMouseClicked

