    public VentanaTablaReservaPorCliente() {
        initComponents();
        String[] cab = {"nombre", "rut", "fecha de atencion", "descripcion", "id_agenda", "id_medico"};//La cebezera de la tabla
        String[][] datos = {};
        modelo = new DefaultTableModel(datos, cab) {//Se ingresa los datos al nuevo modelo ingresado, y se bloquean las filas editables
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tbHorasReservadas.setModel(modelo);//Se inserta el modelo creado anteriormente a la tabla de la ventana
        tbHorasReservadas.addMouseListener(new MouseAdapter() {//Se inserta un evento a la tabla que reconozca el Mouse
        });
        for (org.datacontract.schemas._2004._07.backend.PacienteNoPago dato : clases.FuncionesPaciente.listadoPacienteNoPago().getPacienteNoPago()) {//Se cargaran las reservas a la tabla
            Object[] elementos = {dato.getNombre().getValue(), dato.getRutPaciente().getValue(), dato.getFechaReserva().getDay()+"/"+dato.getFechaReserva().getMonth()+"/"+dato.getFechaReserva().getYear(), dato.getDescripcion().getValue() , dato.getIdAgenda(), dato.getIdMedico()};
            modelo.addRow(elementos);
        }
        
        TableColumnModel tcm = tbHorasReservadas.getColumnModel();//Se obtienen las columnas de la tabla
        tcm.removeColumn(tcm.getColumn(5));//Se elimina la columna con el indice 5(id_medico)
        tcm.removeColumn(tcm.getColumn(4));//Se elimina la columna con el indice 4(id_agenda)
    }

