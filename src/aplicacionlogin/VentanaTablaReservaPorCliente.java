    private void tbHorasReservadasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbHorasReservadasMouseClicked
        if (evt.getClickCount() == 2) {//Si se dan dos click aun elemento de la tabla, se executaran las siguientes acciones...
            int row = tbHorasReservadas.rowAtPoint(evt.getPoint());//Se obtiene la fila seleccionada
            try {
                String rut = tbHorasReservadas.getValueAt(row, 1).toString();//Se obtiene el rut de la fila..
                String id_agenda = tbHorasReservadas.getModel().getValueAt(row, 4).toString();//Se obtiene el id_agenda de la tabla..
                String id_medico = tbHorasReservadas.getModel().getValueAt(row, 5).toString();//Se obtiene el id_medico de la tabla..
                VentanaPago vp = new VentanaPago(rut, id_medico, id_agenda);
                vp.setVisible(true);//Se visualizara VentanaPago y se cierra esta ventana
                this.setVisible(false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: "+ e.getMessage());
            }
        }
    }//GEN-LAST:event_tbHorasReservadasMouseClicked
