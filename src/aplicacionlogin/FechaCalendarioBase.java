    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        modelo_agEliminar.setRowCount(0);
        if (dtxtFecha.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Debe ingresar una fecha en el campo especificado");
        } else {
            Medico med = (Medico) cmbMedico.getSelectedItem();
            c.setTime(dtxtFecha.getDate());
            try {
                date2 = DatatypeFactory.newInstance().newXMLGregorianCalendar(c);
                ArrayOfAgendaMedico arrA = FuncionesMedico.listadoDeHoras_1(med.getRut(), date2);//Se obtiene el listado de horas
                for (org.datacontract.schemas._2004._07.backend.AgendaMedico agenda : arrA.getAgendaMedico()) {//Se ingresan los datos del listado de horas a la tablas
                    String fecha = agenda.getFecha().getDay() + "/" + agenda.getFecha().getMonth() + "/" + agenda.getFecha().getYear();
                    Object[] elementos = {agenda.getIdAgendaMedico(), fecha, agenda.getIdMedico(), clases.FuncionesGenerales.darFormatoHora(agenda.getHoras()) + ":" + clases.FuncionesGenerales.darFormatoHora(agenda.getMinutos()), agenda.getDescripcion().getValue()};
                    modelo_agEliminar.addRow(elementos);
                }
                if (tAgendaMedicoEliminar.getColumnModel().getColumnCount() > 3) { //Si el numero de columnas es superior a 3, se eliminaran  'id_agenda' e 'id_medico' de la tabla vizualisada en la pantalla
                    tcm = tAgendaMedicoEliminar.getColumnModel();
                    tcm.removeColumn(tcm.getColumn(2));
                    tcm.removeColumn(tcm.getColumn(0));
                }
            } catch (DatatypeConfigurationException ex) {
                Logger.getLogger(FechaCalendarioBase.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

    private void tAgendaMedicoEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tAgendaMedicoEliminarMouseClicked
        if (evt.getClickCount() == 2) {
            int row = tAgendaMedicoEliminar.rowAtPoint(evt.getPoint());//Obtiene la fila seleccionada
            try {
                int id_agen = Integer.parseInt(tAgendaMedicoEliminar.getModel().getValueAt(row, 0).toString());//Se obtiene el id de la agenda seleciconada
                int opcion = JOptionPane.showConfirmDialog(this, "Seguro desea eliminar este elemento", "¿Eliminar?", JOptionPane.YES_NO_OPTION);//Se preguntara si desea eleiminar un elemento
                if (opcion == JOptionPane.YES_OPTION) {//Si se elije "Si" se haran las siguietes acciones
                    if (FuncionesMedico.eliminarHora(id_agen)) {//Al eliminar, si devuelve true, se executara ls siguientes acciones
                        JOptionPane.showMessageDialog(this, "Hora eliminada exitosamene");
                        modelo_agEliminar.setRowCount(0);
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_tAgendaMedicoEliminarMouseClicked

