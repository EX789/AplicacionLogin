    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        try {
            c.setTime(dtxtFechaNuevaHora.getDate());//Se obtiene la fecha de la ventana
            date2 = DatatypeFactory.newInstance().newXMLGregorianCalendar(c); //De convierte la fecha anterior a un formato XML
            DateFormat formatoFecha = new SimpleDateFormat("EEEE");//Se creara un objeto que convertira el dia de la fecha en String
            String nuevaFecha = formatoFecha.format(dtxtFechaNuevaHora.getDate());//Se formateara la fecha anterior
            Medico med = (Medico) cmbMedicoAgregar.getSelectedItem();
            if (nuevaFecha.equalsIgnoreCase("sabado") && Integer.parseInt(cbHora.getSelectedItem().toString()) > 14) {//Se preguntara si la fecha seleccionada es del dia sabado y la hora es mayor a las 14
                JOptionPane.showMessageDialog(this, "Error, los dias sabados solo estan disponibles hasta las 14 horas");
            } else { //Sino, se crear ala fecha y obtendra un mensaje
                int opcion = JOptionPane.showConfirmDialog(this, "¿Esta seguro que desea agregar una hora a la agenda de este medico", "¿Seguro?", JOptionPane.YES_NO_OPTION);
                if (opcion == JOptionPane.YES_OPTION) {
                    clases.FuncionesMedico.creaHora(date2, med.getRut(), Integer.parseInt(cbHora.getSelectedItem().toString()), Integer.parseInt(cbMinuto.getSelectedItem().toString()), cbDisponible.getSelectedItem().toString());
                    JOptionPane.showMessageDialog(this, "Ingresado Correctamente");

                }
            }
        } catch (DatatypeConfigurationException ex) {
            Logger.getLogger(FechaCalendarioBase.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton8ActionPerformed

